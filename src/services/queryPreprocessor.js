/**
 * Preprocess a user query before RAG retrieval.
 *
 * Pipeline:
 *  1. Normalize whitespace & punctuation
 *  2. Expand contractions     ("don't" → "do not")
 *  3. Expand abbreviations    ("e.g." → "for example")
 *  4. Spell-correct each word ("hary" → "harry", "beleive" → "believe")
 *  5. Expand synonyms         (adds related terms for the embedding model)
 *
 * @param {string} raw  Raw user question
 * @returns {{ cleaned: string, corrected: string, enriched: string }}
 *   - cleaned:   Contractions + abbreviations expanded
 *   - corrected: Same as cleaned, but with spelling corrected
 *   - enriched:  Ready-for-embedding version with synonym expansion
 */
function preprocessQuery(raw) {
  if (!raw || typeof raw !== "string") {
    return { cleaned: "", corrected: "", enriched: "" };
  }

  // Step 0: Trim & collapse whitespace
  let text = raw.trim().replace(/\s+/g, " ");

  // Step 1: Expand contractions & abbreviations
  text = expandContractions(text);

  const cleaned = text;

  // Step 2: Spell-correct each word
  const words = text.split(/\s+/);
  const correctedWords = words.map((w) => {
    // Preserve punctuation attached to words
    const m = w.match(/^([^a-zA-Z]*)([a-zA-Z].*[a-zA-Z])([^a-zA-Z]*)$/);
    if (!m) return w;
    const [, pre, core, post] = m;
    return pre + correctWord(core) + post;
  });
  const corrected = correctedWords.join(" ");

  // Step 3: Synonym enrichment for the embedding model
  const enriched = expandSynonyms(corrected);

  return { cleaned, corrected, enriched };
}

