/**
 * Check if a word looks like a valid English word (case-insensitive).
 */
function isInDictionary(word) {
  return DICTIONARY.has(word.toLowerCase());
}

/**
 * Attempt to correct a single misspelled word.
 * Strategy (from safest to riskiest):
 *  1. If already in dictionary → return as-is
 *  2. Exact lookup in MISSPELLINGS map → known correction
 *  3. Transposition of adjacent letters (common typo)
 *  4. Single character insertion/removal (e.g. double letters)
 *  5. Otherwise → return original (better to keep the raw word than
 *     risk a false-positive correction)
 */
function correctWord(word) {
  const lower = word.toLowerCase();

  // 1. Already in dictionary or too short to correct
  if (isInDictionary(lower) || lower.length <= 2) return word;

  // 2. Exact misspelling map lookup
  if (MISSPELLINGS[lower]) return MISSPELLINGS[lower];

  // 3. Try swapping adjacent characters (transposition is ~20% of typos)
  for (let i = 0; i < lower.length - 1; i++) {
    const candidate = lower.slice(0, i) + lower[i + 1] + lower[i] + lower.slice(i + 2);
    if (isInDictionary(candidate) || MISSPELLINGS[candidate]) {
      return candidate;
    }
  }

  // 4. Try removing one character (catches double-letter typos like "harrry")
  for (let i = 1; i < lower.length - 1; i++) {
    const candidate = lower.slice(0, i) + lower.slice(i + 1);
    if (isInDictionary(candidate) || MISSPELLINGS[candidate]) {
      return candidate;
    }
  }

  // 5. Give up — return original word
  return word;
}

/**
 * Normalize contractions and abbreviations in text.
 */
function expandContractions(text) {
  let result = text;
  // Word-boundary replacement for contractions
  for (const [short, full] of Object.entries(CONTRACTIONS)) {
    const regex = new RegExp(`\\b${short}\\b`, "gi");
    result = result.replace(regex, full);
  }
  // Abbreviation expansion — uses a broader boundary pattern to handle
  // dotted abbreviations like "e.g." and "i.e."
  for (const [abbr, full] of Object.entries(ABBREVIATIONS)) {
    const escaped = abbr.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const regex = new RegExp(`(^|[\\s(])${escaped}([\\s.,;!?)\\]\\-]|$)`, "gi");
    result = result.replace(regex, (_, before, after) => before + full + after);
  }
  return result;
}

/**
 * Expand a query with synonyms to improve semantic recall.
 * Appends `OR <synonym>` style phrases for the most significant content words.
 */
function expandSynonyms(text) {
  const words = text.split(/\s+/);
  const expanded = [];
  for (const word of words) {
    expanded.push(word);
    const lower = word.toLowerCase().replace(/^[^a-z]+|[^a-z]+$/g, "");
    const synonyms = SYNONYM_EXPANSIONS[lower];
    if (synonyms && synonyms.length > 0) {
      // Append the first 2 most common synonyms to the query
      // This enriches the token space for the embedding model
      expanded.push(synonyms.slice(0, 2).join(" "));
    }
  }
  return expanded.join(" ");
}

